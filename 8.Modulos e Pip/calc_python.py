'''
Este módulo criado para aprimorar os conhecimento em Python, realiza as operações de soma e subtração.

'''


def soma(num1,num2):
    '''
    Retorna a soma de dois números
    '''
    return num1 + num2

def subtrai(num1,num2):
    '''
    Retorna a subtração de dois numeros
    '''
    return num1 - num2